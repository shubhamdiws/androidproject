package com.example.spring_the_string;

public class list_model {

    int id;
    String image;

    public list_model(int id,String image){
        this.id=id;
        this.image=image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}




